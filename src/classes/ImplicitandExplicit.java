package classes;

public class ImplicitandExplicit {

	public static void main(String[] args) {
		//implicit
		int x ='a';
		System.out.println(x);

		//explicit
		int y = 130;
		float f = 100f;
		//float f1 = (float)1-11.00;
		byte b = (byte) y;
		System.out.println(b);
		int y2 = (int) 120.4;
		System.out.println(y2);
	}

}
